package rex.types;

public class Span {
	private Integer start;
	private Integer end;
	
	public Integer getStart() {
		return start;
	}
	public void setStart(Integer value) {
		this.start = value;
	}
	
	public Integer getEnd() {
		return end;
	}
	
	public void setEnd(Integer value) {
		this.end = value;
	}
	
	
}
